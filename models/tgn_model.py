def _self_test() -> None:
    """Discovers real production shards under graph/output/, runs one
    forward pass on the first shard found, and verifies edge count,
    memory update, embedding size, absence of NaN, and device
    consistency. No fabricated data, no synthetic shortcuts."""
    project_root = Path(__file__).resolve().parent.parent
    graph_output_root = project_root / "graph" / "output"

    logger.info("=" * 70)
    logger.info("TGN PRODUCTION SELF-TEST")
    logger.info("root=%s", graph_output_root)
    logger.info("=" * 70)

    config = TGNConfig()
    model, shard_map = build_tgn_from_root(graph_output_root, config)
    model.eval()

    first_relation = next(iter(shard_map))
