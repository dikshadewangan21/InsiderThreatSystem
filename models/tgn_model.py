"""
Temporal Graph Network (TGN) — Production Module
==================================================

Rewritten from scratch. Consumes ONLY the production outputs already
generated by the upstream pipeline stages:

    build_event_graph.py  -> graph/output/node_graph_skeleton.pt
                              graph/output/preprocessing_artifacts.pkl
    edge_features.py      -> graph/output/edge_feature_shards/<Relation>/edge_features_*.pt
    edge_weighting.py     -> (dynamic edge weight folded into the feature
                               shards above; see `_resolve_edge_weight`)

This module never touches `edge_shards/` or `edge_shard_manifest.json` —
those belong to graph construction, not to the TGN.

Run directly from the project root:

    python models/tgn_model.py

No sys.path manipulation, no PYTHONPATH, no dependency on a package
named "graph" — this file is fully self-contained and only imports
torch and the standard library.
"""

from __future__ import annotations

import logging
from dataclasses import dataclass
from pathlib import Path
from typing import Dict, List, Optional, Tuple, Union

import torch
import torch.nn as nn

